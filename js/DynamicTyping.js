//Dynamic Typing

let x = 10;
console.log(typeof x);

x = "Viraj Dilshan";
console.log(typeof x);

x = true;
console.log(typeof x);

x = null;
console.log(typeof x);

x = undefined;
console.log(typeof x);

x = { name: "Viraj Dilshan", age: 22 };
console.log(typeof x);

x = [10, 20, 30];
console.log(typeof x);
console.log(Array.isArray(x));
